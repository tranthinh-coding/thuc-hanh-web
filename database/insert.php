    }
    $values[$k] = $x;
  }

  $values = implode(",", $values);

  $sql = "INSERT INTO san_pham (ten_san_pham, gia, so_luong, so_luong_da_ban, the_loai, mo_ta, hinh_anh) 
    VALUES ($values)";

  mysqli_query($conn, $sql);
}

